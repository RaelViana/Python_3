
"""
    Programa que recebe duas notas pelo usuário e calcula sua média

"""


nota1 = int(input('Digite a primeira nota: '))
nota2 = int(input('Digite a segunda nota: '))

media = (nota1 + nota2)/2

print('A 1°nota foi {}\n A 2° nota foi {}\nA média é  = {}'.format(nota1, nota2, media))




