"""
    Programa que converte temperaturas

"""

c = float(input('Informe  a temperatura em C°: ')) # Recebe do usuário valor para Graus Celsius
f = ((9 * c) / 5) + 32   # Converte valor prara Fahrenheit

print('A Temperatura de {:.0f} °C, corresponde a {:.0f} °F !'.format(c, f)) #print do valor resultante




