"""
    Soma entre dois Números-
    programa que recebe dois números e efetua a soma entre eles.
"""

num1 = float(input('Digite o primeiro número: ')) # recebe valor real digitado pelo usuário
num2 = float(input('Digite o segundo número: '))

s = num1+num2  #  efetua a operação entre as variáveis

print('A soma entre {} e {} é = {}'.format(num1, num2, s)) # imprime o valores digitados e resultado
